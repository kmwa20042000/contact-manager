const mongoose = require('mongoose');

const ContactSchema = mongoose.Schema({
  // since everyone has its own private contact list therefore needs to bring in user module
  user: {
    type: mongoose.Schema.Types.ObjectId,
    ref: 'users',
  },
  name: {
    type: String,
    required: true,
  },
  email: {
    type: String,
    required: true,
  },
  phone: {
    type: String,
  },
  type: {
    type: String,
    default: 'personal',
  },
  date: {
    type: Date,
    default: Date.now,
  },
});
module.exports = mongoose.model('contact', ContactSchema);
